<h1>About</h1>
<strong></strong>
<p><strong>ALC (Active Learning Club) adalah lembaga edukasi yang didirikan oleh 2 mahasiswa ITB sejak Februari 2011</strong>
yang memiliki concern terhadap dunia Olimpiade Sains Nasional dan pembinaan privat siswa SMA.</p>

<p>Sesuai dengan namanya, lembaga ini bergerak di bidang pendidikan olimpiade dalam membina pelajar SMA untuk
meraih prestasi terbaik pada kompetisi olimpiade sains, baik skala nasional maupun internasional.

Selain itu, <strong>ALC</strong> juga berfokus kepada peningkatan prestasi belajar  siswa di sekolah melalui bimbingan privat eksklusif.

<strong>ALC</strong> bertekad membantu pembentukan karakter fairness dan toughness
di kalangan pelajar dengan cara memotivasi dan berbagi pengalaman kepada para pelajar.</p>
